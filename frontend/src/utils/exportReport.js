/**
 * Open the HTML report in a new window and trigger the browser print dialog
 * with the page already styled for print. The user picks "Save as PDF" as
 * their destination — same approach Notion / Linear / GitHub use.
 *
 * Why not bundle jsPDF? It's 2MB+, the output looks worse than print-to-PDF,
 * and the browser already does this perfectly.
 */
export function exportAnalysisAsPDF(analysis, opts = {}) {
  const html = buildHTMLReport(analysis, opts);

  // Inject a print stylesheet + auto-trigger script before </head>
  const printAugmented = html.replace(
    '</head>',
    `<style>
      @media print {
        body { margin: 0; padding: 24px; }
        .no-print, button { display: none !important; }
        @page { size: A4; margin: 18mm 14mm; }
      }
    </style>
    <script>
      window.addEventListener('load', function () {
        // Small delay so fonts/layout settle before the print dialog opens
        setTimeout(function () { window.print(); }, 250);
      });
      // Auto-close the popup after the user finishes printing/saving
      window.addEventListener('afterprint', function () {
        setTimeout(function () { window.close(); }, 300);
      });
    </script>
    </head>`
  );

  const popup = window.open('', '_blank', 'width=900,height=1100');
  if (!popup) {
    // Popup blocked — fall back to downloading the HTML so the user can
    // open it themselves and print from there.
    // eslint-disable-next-line no-alert
    alert(
      'Popup blocked. Please allow popups for this site to export as PDF, ' +
      'or use the HTML export and print from your browser.'
    );
    return;
  }
  popup.document.open();
  popup.document.write(printAugmented);
  popup.document.close();
}
