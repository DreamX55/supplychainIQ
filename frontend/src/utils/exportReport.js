const SEVERITY_HTML = {
  Critical: { color: '#dc2626', bg: '#fef2f2', border: '#fecaca' },
  High:     { color: '#ea580c', bg: '#fff7ed', border: '#fed7aa' },
  Medium:   { color: '#d97706', bg: '#fffbeb', border: '#fde68a' },
  Low:      { color: '#059669', bg: '#ecfdf5', border: '#a7f3d0' },
};

function escapeHtml(s) {
  if (s == null) return '';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function sevBadge(level) {
  const s = SEVERITY_HTML[level] || SEVERITY_HTML.Medium;
  return `<span class="sev-badge" style="color:${s.color};background:${s.bg};border-color:${s.border};">${escapeHtml(level || 'Unknown')}</span>`;
}

