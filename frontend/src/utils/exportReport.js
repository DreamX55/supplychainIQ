/**
 * Export utilities for SupplyChainIQ analyses.
 *
 * Generates a clean Markdown report from an analysis dict and triggers
 * a browser download via Blob URL. No backend round-trip — the analysis
 * is already in memory on the client.
 */

const SEVERITY_EMOJI = {
  Critical: '🔴',
  High: '🟠',
  Medium: '🟡',
  Low: '🟢',
};

function fmtPct(n) {
  if (typeof n !== 'number') return '—';
  return `${Math.round(n * 100)}%`;
}

function safeArr(x) {
  return Array.isArray(x) ? x : [];
}

/**
 * Build a Markdown string from a SupplyChainIQ analysis.
 */
export function buildMarkdownReport(analysis, opts = {}) {
  if (!analysis) return '';

  const date = new Date().toISOString().slice(0, 10);
  const company = opts.companyName || 'Your Company';
  const lines = [];

  lines.push(`# SupplyChainIQ Risk Brief`);
  lines.push('');
  lines.push(`**Prepared for:** ${company}  `);
  lines.push(`**Date:** ${date}  `);
  if (analysis.overall_risk_level) {
    lines.push(`**Overall risk:** ${SEVERITY_EMOJI[analysis.overall_risk_level] || ''} ${analysis.overall_risk_level}  `);
  }
  if (analysis.provider_meta?.provider_used) {
    lines.push(
      `**AI engine:** ${analysis.provider_meta.provider_used}${
        analysis.provider_meta.is_mock ? ' (demo mode)' : ''
      }`
    );
  }
  lines.push('');

  if (analysis.summary) {
    lines.push(`## Executive summary`);
    lines.push('');
    lines.push(analysis.summary);
    lines.push('');
  }

  const riskNodes = safeArr(analysis.risk_nodes);
  if (riskNodes.length > 0) {
    lines.push(`## Risk nodes (${riskNodes.length})`);
    lines.push('');
    riskNodes.forEach((r, i) => {
      lines.push(`### ${i + 1}. ${SEVERITY_EMOJI[r.risk_level] || ''} ${r.node || 'Unnamed node'}`);
      lines.push('');
      lines.push(`- **Severity:** ${r.risk_level || 'Unknown'}`);
      lines.push(`- **Category:** ${r.category || 'general'}`);
      lines.push(`- **Confidence:** ${fmtPct(r.confidence_score)}`);
      lines.push('');
      if (r.cause) {
        lines.push(`**Cause.** ${r.cause}`);
        lines.push('');
      }
      const evidence = safeArr(r.evidence);
      if (evidence.length > 0) {
        lines.push(`**Grounded in:**`);
        evidence.forEach((e) => lines.push(`- ${e}`));
        lines.push('');
      }
      if (r.recommended_action) {
        lines.push(`**Recommended action.** ${r.recommended_action}`);
        lines.push('');
      }
    });
  }

  const graph = analysis.supply_chain_graph;
  if (graph && safeArr(graph.nodes).length > 0) {
    lines.push(`## Supply chain graph`);
    lines.push('');
    const byRole = {};
    graph.nodes.forEach((n) => {
      const r = n.role || 'other';
      if (!byRole[r]) byRole[r] = [];
      byRole[r].push(n);
    });
    ['supplier', 'factory', 'port', 'destination', 'other'].forEach((role) => {
      const list = byRole[role];
      if (!list || list.length === 0) return;
      const label = role.charAt(0).toUpperCase() + role.slice(1) + 's';
      lines.push(`**${label}:** ${list.map((n) => n.label + (n.location ? ` (${n.location})` : '')).join(', ')}`);
      lines.push('');
    });
  }

  const followups = safeArr(analysis.follow_up_suggestions);
  if (followups.length > 0) {
    lines.push(`## Suggested next questions`);
    lines.push('');
    followups.forEach((q) => lines.push(`- ${q}`));
    lines.push('');
  }

  lines.push('---');
  lines.push(`*Generated by SupplyChainIQ — AI-powered supply chain risk intelligence for SMEs.*`);

  return lines.join('\n');
}

/**
 * Download a string as a file via the browser.
 */
export function downloadTextFile(filename, content, mimeType = 'text/markdown') {
  const blob = new Blob([content], { type: `${mimeType};charset=utf-8` });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  // Free the blob URL on the next tick so the click has time to fire
  setTimeout(() => URL.revokeObjectURL(url), 0);
}

/**
 * One-shot helper: build the markdown and trigger the download.
 */
export function exportAnalysisAsMarkdown(analysis, opts = {}) {
  const md = buildMarkdownReport(analysis, opts);
  const stamp = new Date().toISOString().slice(0, 10);
  const slug = (opts.companyName || 'supplychainiq').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
  downloadTextFile(`${slug}-risk-brief-${stamp}.md`, md);
}

